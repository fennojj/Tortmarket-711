export function simulateReach(campaign: Campaign, _elapsedMs: number): CampaignMetrics {
  const launched = campaign.launchedAt ?? campaign.createdAt;
  const ageMin = Math.max(0, (Date.now() - launched) / 60_000);
  const curve = 1 - Math.exp(-ageMin / 45);
  const channelMultiplier = campaign.channels.length;
  const base = campaign.priority * 90 + campaign.channels.length * 320;
  const viralCoefficient = campaign.kind === "whale_alert" ? 1.6 :
    campaign.kind === "throne_taken" ? 1.4 :
    campaign.kind === "resolution_imminent" ? 1.5 :
    campaign.kind === "fomo_blast" ? 1.3 : 1.0;
  const target = base * channelMultiplier * (0.8 + Math.random() * 0.5) * viralCoefficient;
  const impressions = Math.round(target * curve);
  const ctr =
    0.018 + Math.random() * 0.04 +
    (campaign.tone === "hype" ? 0.015 : 0) +
    (campaign.tone === "urgent" ? 0.012 : 0) +
    (campaign.kind === "fomo_blast" ? 0.02 : 0);
  const clicks = Math.round(impressions * ctr);
  const signupRate = campaign.kind === "onboarding_push" ? 0.22 :
    campaign.kind === "fomo_blast" ? 0.10 :
    campaign.kind === "resolution_imminent" ? 0.08 : 0.06;
  const signups = Math.round(clicks * signupRate);
  const plays = Math.round(clicks * 0.14);
  return {
    impressions,
    clicks,
    signups,
    plays,
    ctr: Number((ctr * 100).toFixed(2)),
  };
}

