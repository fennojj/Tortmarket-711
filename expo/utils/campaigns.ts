import type { AlertItem, Market, User } from "@/types";
import type { MarketEdge } from "@/utils/signals";

export type CampaignKind =
  | "play_hype"
  | "whale_alert"
  | "winner_spotlight"
  | "streak_milestone"
  | "market_mover"
  | "resolution"
  | "throne_taken"
  | "daily_recap"
  | "onboarding_push"
  | "fomo_blast"
  | "resolution_imminent";

export type CampaignChannel = "x" | "reddit" | "discord" | "push" | "email";

export type CampaignStatus = "drafting" | "scheduled" | "live" | "completed";

export interface CampaignMetrics {
  impressions: number;
  clicks: number;
  signups: number;
  plays: number;
  ctr: number;
}

export interface CampaignPost {
  channel: CampaignChannel;
  body: string;
  hashtags?: string[];
}

export interface Campaign {
  id: string;
  kind: CampaignKind;
  status: CampaignStatus;
  priority: number;
  headline: string;
  tagline: string;
  tone: "celebrate" | "hype" | "urgent" | "info" | "respect";
  channels: CampaignChannel[];
  posts: CampaignPost[];
  cta: { label: string; route?: string };
  marketId?: string;
  handle?: string;
  reasons: string[];
  metrics: CampaignMetrics;
  createdAt: number;
  launchedAt?: number;
  completedAt?: number;
  autoGenerated: boolean;
}

const HASH_CORE = ["#TortSite", "#MassTort", "#PredictionMarkets"];

function fmt(n: number): string {
  if (n >= 1_000_000) return `${(n / 1_000_000).toFixed(1)}M`;
  if (n >= 1_000) return `${(n / 1_000).toFixed(1)}K`;
  return `${n}`;
}

function pick<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

function emptyMetrics(): CampaignMetrics {
  return { impressions: 0, clicks: 0, signups: 0, plays: 0, ctr: 0 };
}

function stableHour(): string {
  return String(Math.floor(Date.now() / 3_600_000));
}

function stableDay(): string {
  return new Date().toDateString();
}

export function buildPlayHypeCampaign(args: {
  play: AlertItem;
  market: Market | undefined;
  edge?: MarketEdge;
}): Campaign {
  const { play, market, edge } = args;
  const caseName = market?.caseName ?? "a live tort";
  const side = play.side ?? "YES";
  const shares = play.shares ?? 0;
  const cost = play.cost ?? 0;
  const price = side === "YES" ? market?.yesPrice ?? 50 : market?.noPrice ?? 50;
  const isWhale = cost >= 10_000;

  const headline = isWhale
    ? `${play.author} loads ${fmt(cost)} pts on ${caseName}`
    : `${play.author} fires on ${caseName}`;

  const tagline = isWhale
    ? `Whale desk lit up. ${fmt(shares)} shares ${side} @ ${price}¢ — the tape is moving.`
    : `${fmt(shares)} shares ${side} @ ${price}¢. Who's riding the wave?`;

  const reasons: string[] = [];
  if (edge) {
    reasons.push(
      `Model edge ${edge.edge >= 0 ? "+" : ""}${edge.edge.toFixed(1)}¢`,
      `Confidence ${edge.confidence.toFixed(0)}%`,
    );
    if (edge.reasons[0]) reasons.push(edge.reasons[0]);
  }
  if (isWhale) reasons.push("Whale ticket");

  const posts: CampaignPost[] = [
    {
      channel: "x",
      body: `${headline}\n\n${fmt(shares)} shares ${side} @ ${price}¢ · ${fmt(cost)} pts on the line.${edge ? ` Model fair: ${edge.fairYes.toFixed(0)}¢.` : ""}\n\nFeed is live ↓`,
      hashtags: [...HASH_CORE, `#${(caseName.split(" ")[0] ?? "Tort").replace(/[^A-Za-z]/g, "")}`],
    },
    {
      channel: "reddit",
      body: `[Play Alert] ${play.author} just went ${side} on ${caseName} — ${fmt(shares)} shares @ ${price}¢ (${fmt(cost)} pts).${edge ? `\n\nTortCast model fair value sits at ${edge.fairYes.toFixed(0)}¢ YES (${edge.confidence.toFixed(0)}% confidence). ${edge.reasons.slice(0, 2).join(" · ")}.` : ""}\n\nDiscuss the docket, the Daubert, and whether this rips.`,
    },
    {
      channel: "discord",
      body: `**${headline}**\n${side} · ${fmt(shares)} sh · ${price}¢ · ${fmt(cost)} pts${isWhale ? " · 🐋 whale-tier" : ""}${edge ? `\nEdge ${edge.edge.toFixed(1)} · Conf ${edge.confidence.toFixed(0)}%` : ""}`,
    },
    {
      channel: "push",
      body: `${play.author} just took ${side} on ${caseName}. Price ${price}¢. Tap in.`,
    },
  ];

  return {
    id: `cmp-play-${play.id}`,
    kind: isWhale ? "whale_alert" : "play_hype",
    status: "drafting",
    priority: isWhale ? 92 : 70,
    headline,
    tagline,
    tone: isWhale ? "hype" : "celebrate",
    channels: isWhale ? ["x", "reddit", "discord", "push"] : ["x", "discord", "push"],
    posts,
    cta: { label: "See the market", route: market ? `/market/${market.id}` : undefined },
    marketId: market?.id,
    handle: play.author,
    reasons,
    metrics: emptyMetrics(),
    createdAt: Date.now(),
    autoGenerated: true,
  };
}

